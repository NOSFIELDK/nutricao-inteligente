function img(prompt: string, _imageSize: string) {
  let h = 0;
  for (let i = 0; i < prompt.length; i++) h = (Math.imul(31, h) + prompt.charCodeAt(i)) | 0;
  return `https://picsum.photos/seed/${Math.abs(h)}/800/450`;
}

